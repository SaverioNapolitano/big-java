package com.snapolitano.exercises.practice.chapter1;

/**
 * Translation of some phrases from English to Italian
 */
public class E1_21 {
    public static void translatePhrases() {
        System.out.println("------------------------------------------------------------------");
        System.out.println("|   List of Phrases to Translate   |        Translation          |");
        System.out.println("------------------------------------------------------------------");
        System.out.println("| Good morning.                    | Buongiorno.                 |");
        System.out.println("------------------------------------------------------------------");
        System.out.println("| It is a pleasure to meet you.    | E' un piacere conoscerti.   |");
        System.out.println("------------------------------------------------------------------");
        System.out.println("| Please call me tomorrow.         | Per favore chiamami domani. |");
        System.out.println("------------------------------------------------------------------");
        System.out.println("| Have a nice day!                 | Buona giornata!             |");
        System.out.println("------------------------------------------------------------------");
    }

    public static void main(String[] args) {
        translatePhrases();
    }
}
